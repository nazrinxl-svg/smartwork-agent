import fs from "node:fs";
import path from "node:path";

const root = process.cwd();

function write(file, content) {
  const full = path.join(root, file);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, content.endsWith("\n") ? content : content + "\n", "utf8");
  console.log("wrote", file);
}

function readPackage() {
  const raw = fs.readFileSync(path.join(root, "package.json"), "utf8").replace(/^\uFEFF/, "");
  return JSON.parse(raw);
}

function writePackage(pkg) {
  fs.writeFileSync(path.join(root, "package.json"), JSON.stringify(pkg, null, 2) + "\n", "utf8");
  console.log("updated package.json");
}

const pkg = readPackage();
pkg.scripts ||= {};
pkg.scripts["smartwork:dewavps:phase5s:preflight"] = "node scripts/smartwork-dewavps-phase5s-preflight.mjs";
pkg.scripts["smartwork:dewavps:phase5s:report"] = "node scripts/smartwork-dewavps-phase5s-report.mjs";
writePackage(pkg);

