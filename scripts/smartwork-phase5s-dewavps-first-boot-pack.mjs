"    \"SMARTWORK_NO_SIAGA_INPUT=true\",",
"    \"SMARTWORK_NO_BROWSER_OPEN=true\",",
"    \"SMARTWORK_NO_REAL_SAVE=true\",",
"    \"SMARTWORK_NO_REAL_SEND=true\",",
